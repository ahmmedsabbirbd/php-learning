<?php
$n = 12;
$o = 012;
$h = 0x1a;

// decimal
printf("The number is Decimal %d and %d and %d \n", $n, $o, $h);

// binary
printf("The number is Binary %b and %b and %b \n", $n, $o, $h);

// octal
printf("The number is Octal %o and %o and %o \n", $n, $o, $h);

// hex
printf("The number is Hex %x and %x and %x \n", $n, $o, $h);